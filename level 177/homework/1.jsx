// React-დან ვაიმპორტებთ useState კაუჭს
import { useState } from "react";

/*
კომენტარებით ახსნა:

კაუჭი (Hook) არის React-ის ფუნქცია,
რომელიც გვაძლევს საშუალებას ფუნქციურ კომპონენტებში
ვიყენოთ ისეთი შესაძლებლობები,
რაც ადრე მხოლოდ class კომპონენტებში იყო შესაძლებელი
(მაგალითად: state, lifecycle და სხვა).
*/

/*
რატომ შექმნეს კაუჭები (Hooks)?
- კოდი უფრო მარტივი და მოკლე რომ ყოფილიყო
- class კომპონენტების გამოყენება რომ აღარ დაგვჭირვებოდა
- ლოგიკის გამეორება მარტივად რომ შეგვეძლოს
*/

/*
როდის ვიყენებთ კაუჭებს?
- როცა გვჭირდება მონაცემის შეცვლა (state)
- როცა გვჭირდება გვერდითი მოქმედებები (მაგ: API)
- როცა ფუნქციურ კომპონენტში გვინდა React-ის ფუნქციების გამოყენება
*/

/*
useState კაუჭი რა არის?
useState არის კაუჭი,
რომელიც გვაძლევს საშუალებას შევინახოთ და შევცვალოთ მონაცემი
ფუნქციურ კომპონენტში.
*/

/*
როგორ ვიყენებთ useState-ს?
useState აბრუნებს ორ რამეს:
1) მონაცემს (state)
2) ფუნქციას, რომელიც ამ მონაცემს ცვლის
*/

// ===== პირველი მაგალითი =====
function Counter() {
  // count არის მონაცემი
  // setCount არის ფუნქცია, რომელიც count-ს ცვლის
  const [count, setCount] = useState(0);

  return (
    <div>
      {/* ვაჩვენებთ count-ის მნიშვნელობას */}
      <h2>რიცხვი: {count}</h2>

      {/* ღილაკზე დაჭერისას count იზრდება */}
      <button onClick={() => setCount(count + 1)}>
        გაზრდა
      </button>
    </div>
  );
}

// ===== მეორე მაგალითი =====
function NameChanger() {
  // name არის ტექსტური მონაცემი
  // setName ცვლის name-ს
  const [name, setName] = useState("უცნობი");

  return (
    <div>
      {/* ვაჩვენებთ სახელს */}
      <h2>სახელი: {name}</h2>

      {/* ღილაკზე დაჭერისას სახელი იცვლება */}
      <button onClick={() => setName("ლუკა")}>
        სახელის შეცვლა
      </button>
    </div>
  );
}

// კომპონენტების ექსპორტი
export { Counter, NameChanger };
